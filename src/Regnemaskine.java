public class Regnemaskine {
    // Attributes
    double x;
    double y;
    // Constructor
    public Regnemaskine(double x ,double y) {
        this.x = x;
        this.y = y;
    }
    // Methods
    public double addition() {
        return x + y;
    }
    public double multiplikation() {
        return x * y;
    }
}
